export interface DadosMedico {
  nomeCompleto: string;
  nomeProfissional?: string;
  especialidade: string;
  cidadeUF: string;
  crm?: string;
  rqe?: string;
  clinica?: string;
  instagram?: string;
  site?: string;
  servicos?: string;
  produtos?: string;
  publicoAlvo?: string;
  objetivo?: string;
  email: string;
  whatsapp: string;
}

export function montarPrompt1(dados: DadosMedico): string {
  return PROMPT_1_BUSCA
    .replace('{{NOME_COMPLETO}}', dados.nomeCompleto || 'Não informado')
    .replace('{{NOME_PROFISSIONAL}}', dados.nomeProfissional || dados.nomeCompleto || 'Não informado')
    .replace('{{ESPECIALIDADE}}', dados.especialidade || 'Não informado')
    .replace('{{CIDADE_UF}}', dados.cidadeUF || 'Não informado')
    .replace('{{CRM}}', dados.crm || 'Não informado')
    .replace('{{RQE}}', dados.rqe || 'Não informado')
    .replace('{{CLINICA}}', dados.clinica || 'Não informado')
    .replace('{{INSTAGRAM}}', dados.instagram || 'Não informado')
    .replace('{{SITE}}', dados.site || 'Não informado')
    .replace('{{SERVICOS}}', dados.servicos || 'Não informado')
    .replace('{{PRODUTOS}}', dados.produtos || 'Não informado')
    .replace('{{PUBLICO_ALVO}}', dados.publicoAlvo || 'Não informado')
    .replace('{{OBJETIVO}}', dados.objetivo || 'Aumentar autoridade e agendamentos');
}

